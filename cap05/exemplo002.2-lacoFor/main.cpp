// Exemplo002.2 - LacoFor: main.cpp
// Repeticao controlada por contador usando a instrucao for
#include <iostream>

using std::cout;
using std::endl;

int main()
{
   // Laco for
   for(int j = 2; j <= 80; j++)
      cout << j << " "; // Exibe o valor actual do controlador j na tela

   cout << endl; // Gera caracter de nova linha

   return 0; // Indica terminacao bem sucedida

} // Fim de main
