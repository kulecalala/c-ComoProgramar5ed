// Escreva quatro instruções C++ diferentes que adicionam 1 à variável do tipo inteiro x.
#include <iostream>

using namespace std;

int main()
{
   int x = 10;

   x++;
   cout << x << endl;

   ++x;
   cout << x << endl;

   x = x + 1;
   cout << x << endl;

   x += 1;
   cout << x << endl;

   return 0;
}
